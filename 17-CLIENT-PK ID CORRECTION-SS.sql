TRUNCATE TABLE T_DAILY_TRANS_PURCHASE_BOM DROP STORAGE ;
        INSERT INTO T_DAILY_TRANS_PURCHASE_BOM
        SELECT
            TIBD.TRANS_ITEM_BOM_ID, TIBD.TRANS_ITEM_ID, TIBD.BOM_VARIANT_ID, TIBD.QUANTITY, TIBD.CONVERSION_FACTOR, TIBD.STOCK_VALUE, TIBD.TRANS_BOM_RATE, TIBD.TRANS_BOM_AMOUNT, 
            IVD.ITEM_GROUP, IVD.ITEM_NAME, IVD.VARIANT_NAME, IVD.ITEM_STOCK_UOM_CODE, IVD.ITEM_TYPE_ID, IVD.ITEM_GROUP_ID, IVD.BASE_ITEM_GROUP_ID, IVD.FINENESS, 
            TIBD.SOR_TRANS_ITEM_BOM_ID, TIBD.DEST_STOCK_BOM_ID, 0 AS WASTAGE_PER 
        FROM V_ITEM_VARIANT_RTN_DTL IVD,V_TRANS_ITEM_BOM_DTL TIBD,T_TRANS_HDR_EXTD EXTD,T_TRANS_ITEM_DTL_EXTD TIDE,T_TRANS_ITEM_DTL TID,T_TRANS_HDR TH
        WHERE
            EXTD.TRANS_ID = TH.TRANS_ID 
            AND TID.TRANS_ID = TH.TRANS_ID
            AND TIBD.BOM_VARIANT_ID = IVD.VARIANT_ID
            AND TIBD.TRANS_ITEM_ID = TID.TRANS_ITEM_ID
            AND TID.TRANS_ITEM_ID = TIDE.TRANS_ITEM_ID
            AND TID.ROW_STATUS = 1
            AND TH.ROW_STATUS = 1
            AND EXISTS (SELECT 1 FROM T_DAILY_TRANS_PURCHASE DUP WHERE TIBD.TRANS_ITEM_ID = DUP.TRANS_ITEM_ID);
        
		UPDATE T_DAILY_TRANS_PURCHASE_BOM DTPB
		SET WASTAGE_PER = NVL((SELECT AVG(TIBFD.ROW_VALUE) FROM T_TRANS_ITEM_BOM_FORMULA_DTL TIBFD,V_FORMULA_PROCEDURE_DTL FPD WHERE DTPB.TRANS_ITEM_BOM_ID = TIBFD.TRANS_ITEM_BOM_ID AND TIBFD.PROCEDURE_ID = FPD.PROCEDURE_ID AND TIBFD.ROW_NO = FPD.ROW_NO AND FPD.ROW_TYPE_ID = 1010),0)
		WHERE EXISTS (SELECT 1 FROM T_DAILY_TRANS_PURCHASE DTP WHERE DTPB.TRANS_ITEM_ID = DTP.TRANS_ITEM_ID);

		UPDATE T_DAILY_TRANS_PURCHASE_BOM DTPB
		SET WASTAGE_PER = NVL((SELECT STD_BUYING_RATE FROM T_TRANS_ITEM_BOM_DTL_EXTD TIBDE WHERE DTPB.TRANS_ITEM_BOM_ID = TIBDE.TRANS_ITEM_BOM_ID),0)
		WHERE EXISTS (SELECT 1 FROM T_DAILY_TRANS_PURCHASE DTP WHERE DTPB.TRANS_ITEM_ID = DTP.TRANS_ITEM_ID )
		AND WASTAGE_PER = 0;
		COMMIT;

